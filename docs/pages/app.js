const LANGUAGE_KEY = "hb_page_lang";
const DEFAULT_LANGUAGE = "en";

function getPreferredLanguage() {
	const saved = localStorage.getItem(LANGUAGE_KEY);
	if (saved === "en" || saved === "de") {
		return saved;
	}
	const browser = (navigator.language || "").toLowerCase();
	return browser.startsWith("de") ? "de" : DEFAULT_LANGUAGE;
}

function getText(lang, key) {
	return I18N[lang]?.[key] || I18N.en[key] || key;
}

function applyLanguage(lang) {
	document.documentElement.lang = lang;
	const languageSwitch = document.getElementById("language-switch");
	if (languageSwitch) {
		languageSwitch.value = lang;
	}
	const elements = document.querySelectorAll("[data-i18n]");
	for (const element of elements) {
		const key = element.getAttribute("data-i18n");
		element.textContent = getText(lang, key);
	}
}

function initLanguageSwitch() {
	const languageSwitch = document.getElementById("language-switch");
	const initialLanguage = getPreferredLanguage();
	applyLanguage(initialLanguage);
	if (!languageSwitch) {
		return initialLanguage;
	}
	languageSwitch.addEventListener("change", () => {
		const nextLang = languageSwitch.value === "de" ? "de" : "en";
		localStorage.setItem(LANGUAGE_KEY, nextLang);
		applyLanguage(nextLang);
	});
	return initialLanguage;
}

function initLightbox() {
	const targets = Array.from(document.querySelectorAll(".lightbox-target"));
	if (targets.length === 0) {
		return;
	}

	const root = document.createElement("div");
	root.className = "lightbox";
	root.innerHTML = `
		<div class="lightbox-inner">
			<button class="lightbox-close" type="button">Close</button>
			<img class="lightbox-image" alt="Screenshot preview">
			<div class="lightbox-caption"></div>
		</div>
	`;
	document.body.appendChild(root);

	const closeButton = root.querySelector(".lightbox-close");
	const image = root.querySelector(".lightbox-image");
	const caption = root.querySelector(".lightbox-caption");
	const close = () => root.classList.remove("is-open");

	for (const target of targets) {
		target.addEventListener("click", () => {
			const lang = getPreferredLanguage();
			image.src = target.src;
			image.alt = target.alt;
			const captionKey = target.dataset.lightboxCaptionKey;
			caption.textContent = captionKey ? getText(lang, captionKey) : target.alt;
			root.classList.add("is-open");
		});
	}

	closeButton.addEventListener("click", close);
	root.addEventListener("click", (event) => {
		if (event.target === root) {
			close();
		}
	});
	document.addEventListener("keydown", (event) => {
		if (event.key === "Escape") {
			close();
		}
	});
}

function detectRepository() {
	const hostname = window.location.hostname;
	const pathnameParts = window.location.pathname.split("/").filter(Boolean);
	const owner = hostname.endsWith(".github.io") ? hostname.replace(".github.io", "") : null;
	const repo = pathnameParts.length > 0 ? pathnameParts[0] : null;
	if (!owner || !repo) {
		return null;
	}
	return { owner, repo };
}

function setDownloadState(target, url) {
	if (!target) {
		return;
	}
	if (!url) {
		target.classList.add("is-disabled");
		target.setAttribute("aria-disabled", "true");
		target.removeAttribute("href");
		return;
	}
	target.classList.remove("is-disabled");
	target.setAttribute("aria-disabled", "false");
	target.href = url;
}

function findAsset(assets, matcher) {
	return assets.find((asset) => matcher(asset.name.toLowerCase()));
}

async function loadLatestRelease(currentLanguage) {
	const statusMessage = document.getElementById("status-message");
	const releaseVersion = document.getElementById("release-version");
	const releaseChannel = document.getElementById("release-channel");
	const releaseDate = document.getElementById("release-date");
	const releaseLink = document.getElementById("release-link");
	const changelogLink = document.getElementById("changelog-link");
	const repoName = document.getElementById("repo-name");
	const windowsTarget = document.getElementById("download-windows");
	const macTarget = document.getElementById("download-macos");
	const linuxTarget = document.getElementById("download-linux");

	if (!statusMessage || !releaseVersion || !releaseChannel || !releaseDate) {
		return;
	}

	const repository = detectRepository();
	if (!repository) {
		statusMessage.textContent = getText(currentLanguage, "status_repo_unknown");
		if (repoName) {
			repoName.textContent = "Unknown";
		}
		return;
	}

	const fullRepo = `${repository.owner}/${repository.repo}`;
	if (repoName) {
		repoName.textContent = fullRepo;
	}

	try {
		const response = await fetch(`https://api.github.com/repos/${fullRepo}/releases`, {
			headers: { Accept: "application/vnd.github+json" },
		});
		if (!response.ok) {
			throw new Error(`GitHub API returned ${response.status}`);
		}
		const releases = await response.json();
		const latestRelease = releases.find((release) => !release.draft);
		if (!latestRelease) {
			statusMessage.textContent = getText(currentLanguage, "status_no_release");
			releaseVersion.textContent = "Unavailable";
			releaseChannel.textContent = "Unavailable";
			releaseDate.textContent = "Unavailable";
			return;
		}

		const assets = latestRelease.assets || [];
		const windowsAsset = findAsset(assets, (name) => name.includes("windows"));
		const macAsset = findAsset(assets, (name) => name.includes("macos"));
		const linuxAsset = findAsset(assets, (name) => name.includes("linux"));

		setDownloadState(windowsTarget, windowsAsset?.browser_download_url);
		setDownloadState(macTarget, macAsset?.browser_download_url);
		setDownloadState(linuxTarget, linuxAsset?.browser_download_url);

		releaseVersion.textContent = latestRelease.tag_name || latestRelease.name || "Unknown";
		releaseChannel.textContent = latestRelease.prerelease
			? getText(currentLanguage, "channel_alpha")
			: getText(currentLanguage, "channel_stable");
		releaseDate.textContent = latestRelease.published_at
			? new Date(latestRelease.published_at).toLocaleDateString(currentLanguage)
			: "Unknown";
		if (releaseLink) {
			releaseLink.href = latestRelease.html_url;
		}
		if (changelogLink) {
			changelogLink.href = latestRelease.html_url;
		}
		statusMessage.textContent = getText(currentLanguage, "status_loaded");
	} catch (error) {
		console.error(error);
		statusMessage.textContent = getText(currentLanguage, "status_api_error");
		releaseVersion.textContent = "Error";
		releaseChannel.textContent = "Error";
		releaseDate.textContent = "Error";
	}
}

const language = initLanguageSwitch();
initLightbox();
void loadLatestRelease(language);
